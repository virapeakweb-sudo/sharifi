    // Mobile Menu Toggle
    function toggleMenu() {
        const menu = document.getElementById('mobileMenu');
        const overlay = document.getElementById('menuOverlay');
        
        if (menu.classList.contains('translate-x-full')) {
            menu.classList.remove('translate-x-full');
            overlay.classList.remove('hidden');
            setTimeout(() => overlay.classList.remove('opacity-0'), 10);
        } else {
            menu.classList.add('translate-x-full');
            overlay.classList.add('opacity-0');
            setTimeout(() => overlay.classList.add('hidden'), 300);
        }
    }

    // Login Toggle
    function toggleLogin() {
        const sheet = document.getElementById('loginSheet');
        const overlay = document.getElementById('loginOverlay');
        const isHidden = sheet.classList.contains('invisible');

        if (isHidden) {
            overlay.classList.remove('hidden');
            setTimeout(() => overlay.classList.remove('opacity-0'), 10);
            sheet.classList.remove('invisible', 'opacity-0'); 
            sheet.classList.remove('translate-y-full');
            sheet.classList.remove('md:translate-y-full');
        } else {
            sheet.classList.add('translate-y-full');
            sheet.classList.add('md:translate-y-full');
            sheet.classList.add('invisible', 'opacity-0');
            overlay.classList.add('opacity-0');
            setTimeout(() => overlay.classList.add('hidden'), 300);
        }
    }
